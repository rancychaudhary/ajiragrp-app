            <h4> 2. Shape and Size:</h4>
            <p style="color: #858585"> Rectangular or square-shaped houses are preferred in Vastu as they are considered
                more auspicious than
                irregularly shaped buildings.</p>

            <h4>3. Room Placement:</h4>
            <p style="color: #858585">Place important rooms like the master bedroom and the puja (prayer) room in the
                southwest part of the house.
                Bedrooms for children can be in the northwest or west, while the living room is typically placed in the
                northeast.</p>

            <h4> 4. Avoiding the Center:</h4>
            <p style="color: #858585"> Avoid having a bedroom, kitchen, or bathroom in the center of the house, as this is
                considered inauspicious in
                Vastu.</p>

            <h4> 5. Balanced Elements:</h4>
            <p style="color: #858585">Ensure a balance of the five elements (earth, water, fire, air, and space) in the
                house. For example,
                incorporate earthy colors, indoor plants, and proper ventilation to maintain this balance.</p>

            <h4> 6. Kitchen Placement:</h4>
            <p style="color: #858585"> The position of the kitchen in the South or East corner of the house or South East is
                best. The cooking stove
                should ideally face east, ensuring that the cook faces the auspicious direction while preparing meals.</p>

            <h4> 7. Bathroom Placement:</h4>
            <p style="color: #858585"> Bathrooms should ideally be located in the north, South, and West of the house.
                Ensure that the toilet seat does
                not face east, northeast, or southwest.</p>

            <h4>8. Borewell</h4>
            <p style="color: #858585"> Before commencing construction, it’s advisable to install a borewell in the
                northeastern area.</p>

